#!/usr/bin/env node
// Mesh onboarding CLI — `npx mesh-app onboard`
// Walks the user from zero to a running Mesh server.

import { promises as fs } from "node:fs";
import { existsSync } from "node:fs";
import path from "node:path";
import os from "node:os";
import net from "node:net";
import readline from "node:readline/promises";
import { stdin, stdout, exit, env, platform } from "node:process";
import { spawn, execFile } from "node:child_process";
import { fileURLToPath } from "node:url";

const PKG_DIR = path.resolve(fileURLToPath(import.meta.url), "..", "..");
const CONFIG_DIR = path.join(os.homedir(), ".config", "mesh-app");
const CONFIG_FILE = path.join(CONFIG_DIR, "last-workspace");

const C = {
  reset: "\x1b[0m",
  dim: "\x1b[2m",
  bold: "\x1b[1m",
  amber: "\x1b[38;5;214m",
  green: "\x1b[38;5;35m",
  red: "\x1b[38;5;196m",
  cyan: "\x1b[38;5;81m",
};
const log = (msg) => stdout.write(msg + "\n");
const dim = (s) => `${C.dim}${s}${C.reset}`;
const ok = (s) => `${C.green}✓${C.reset} ${s}`;
const warn = (s) => `${C.amber}!${C.reset} ${s}`;
const err = (s) => `${C.red}✗${C.reset} ${s}`;
const banner = () => {
  log("");
  log(`  ${C.bold}${C.amber}Mesh${C.reset} ${dim("— from zero to a working agent in one command")}`);
  log("");
};

function parseArgs(argv) {
  const args = { _: [], yes: false, port: null, workspace: null };
  for (let i = 0; i < argv.length; i++) {
    const a = argv[i];
    if (a === "--yes" || a === "-y") args.yes = true;
    else if (a === "--port" || a === "-p") args.port = Number(argv[++i]);
    else if (a === "--workspace" || a === "-w") args.workspace = argv[++i];
    else if (a === "--help" || a === "-h") args.help = true;
    else args._.push(a);
  }
  return args;
}

function execFileP(cmd, argv, opts = {}) {
  return new Promise((resolve, reject) => {
    execFile(cmd, argv, opts, (e, stdoutStr, stderrStr) => {
      if (e) reject(Object.assign(e, { stdout: stdoutStr, stderr: stderrStr }));
      else resolve({ stdout: stdoutStr, stderr: stderrStr });
    });
  });
}

async function commandExists(cmd) {
  try {
    await execFileP(cmd, ["--version"]);
    return true;
  } catch {
    return false;
  }
}

async function checkPrereqs() {
  const node = process.versions.node.split(".").map(Number);
  if (node[0] < 20) {
    log(err(`Node.js 20+ required (you have ${process.versions.node}).`));
    exit(1);
  }
  log(ok(`Node.js ${process.versions.node}`));

  const hasGit = await commandExists("git");
  if (!hasGit) {
    log(err("git not found on PATH. Install git first."));
    exit(1);
  }
  log(ok("git"));

  const hasGh = await commandExists("gh");
  if (!hasGh) {
    log(err("GitHub CLI (gh) not found."));
    log(dim("  Install: https://cli.github.com/  (macOS: brew install gh)"));
    exit(1);
  }
  log(ok("GitHub CLI"));
}

async function loadLastWorkspace() {
  try {
    return (await fs.readFile(CONFIG_FILE, "utf8")).trim();
  } catch {
    return null;
  }
}

async function saveLastWorkspace(p) {
  await fs.mkdir(CONFIG_DIR, { recursive: true });
  await fs.writeFile(CONFIG_FILE, p, "utf8");
}

async function promptWorkspace(rl, args) {
  if (args.workspace) return path.resolve(args.workspace);
  const last = await loadLastWorkspace();
  const def = last || path.resolve(process.cwd(), "mesh-workspace");
  if (args.yes) return def;
  const ans = await rl.question(
    `Workspace directory ${dim(`(.mesh/, repos, .env live here)`)}\n  ${dim(`[default: ${def}]`)} > `
  );
  return path.resolve(ans.trim() || def);
}

async function ensureWorkspace(ws) {
  await fs.mkdir(ws, { recursive: true });
  log(ok(`workspace: ${ws}`));
}

async function ensureEnv(ws, rl, args) {
  const envPath = path.join(ws, ".env");
  let existing = {};
  try {
    const text = await fs.readFile(envPath, "utf8");
    for (const line of text.split("\n")) {
      const m = line.match(/^([A-Z_][A-Z0-9_]*)=(.*)$/);
      if (m) existing[m[1]] = m[2];
    }
  } catch {}

  const apiKey = existing.ANTHROPIC_API_KEY || env.ANTHROPIC_API_KEY || "";
  let finalKey = apiKey;
  if (!finalKey) {
    if (args.yes) {
      log(err("ANTHROPIC_API_KEY not set. Re-run without --yes to enter it interactively, or export it first."));
      exit(1);
    }
    log("");
    log(`${C.bold}Anthropic API key${C.reset} ${dim("— get one at console.anthropic.com")}`);
    finalKey = (await rl.question("  ANTHROPIC_API_KEY > ")).trim();
    if (!finalKey) {
      log(err("Empty key. Aborting."));
      exit(1);
    }
  }

  const ghToken = existing.GITHUB_TOKEN || ""; // populated below from gh
  const lines = [
    "# Generated by mesh-app onboard",
    `ANTHROPIC_API_KEY=${finalKey}`,
    ghToken ? `GITHUB_TOKEN=${ghToken}` : "",
  ].filter(Boolean);
  await fs.writeFile(envPath, lines.join("\n") + "\n", { mode: 0o600 });
  log(ok(`.env written (${envPath})`));
  return { ANTHROPIC_API_KEY: finalKey };
}

async function ensureGhAuth() {
  try {
    await execFileP("gh", ["auth", "status"]);
    log(ok("gh authenticated"));
    return;
  } catch {}

  log("");
  log(`${C.bold}GitHub authentication${C.reset} ${dim("— launching gh auth login --web")}`);
  // Inherit stdio so the device-flow prompt is fully interactive
  const child = spawn("gh", ["auth", "login", "--web", "-h", "github.com", "-p", "https"], {
    stdio: "inherit",
  });
  await new Promise((resolve, reject) => {
    child.on("exit", (code) => (code === 0 ? resolve() : reject(new Error(`gh auth login exited ${code}`))));
    child.on("error", reject);
  });
  log(ok("gh authenticated"));
}

async function getGhToken() {
  try {
    const { stdout: out } = await execFileP("gh", ["auth", "token"]);
    return out.trim();
  } catch {
    return "";
  }
}

async function findFreePort(startPort) {
  for (let p = startPort; p < startPort + 50; p++) {
    const ok = await new Promise((resolve) => {
      const s = net.createServer();
      s.unref();
      s.on("error", () => resolve(false));
      s.listen(p, "127.0.0.1", () => s.close(() => resolve(true)));
    });
    if (ok) return p;
  }
  throw new Error("No free port found");
}

async function ensureBuilt() {
  const nextDir = path.join(PKG_DIR, ".next");
  if (existsSync(path.join(nextDir, "BUILD_ID"))) {
    log(ok("Next.js build present"));
    return;
  }
  log("");
  log(`${C.cyan}Building Mesh${C.reset} ${dim("(one-time, ~30s)…")}`);
  const nextBin = path.join(PKG_DIR, "node_modules", "next", "dist", "bin", "next");
  if (!existsSync(nextBin)) {
    log(err(`next CLI not found at ${nextBin}`));
    log(dim("  This usually means dependencies were not installed in the package directory."));
    exit(1);
  }
  await new Promise((resolve, reject) => {
    const c = spawn(process.execPath, [nextBin, "build"], { cwd: PKG_DIR, stdio: "inherit" });
    c.on("exit", (code) => (code === 0 ? resolve() : reject(new Error(`next build exited ${code}`))));
    c.on("error", reject);
  });
  log(ok("build complete"));
}

function openBrowser(url) {
  const cmd =
    platform === "darwin" ? "open" : platform === "win32" ? "start" : "xdg-open";
  const args = platform === "win32" ? ["", url] : [url];
  try {
    spawn(cmd, args, { detached: true, stdio: "ignore" }).unref();
  } catch {}
}

async function waitForPort(port, timeoutMs = 30_000) {
  const deadline = Date.now() + timeoutMs;
  while (Date.now() < deadline) {
    const up = await new Promise((resolve) => {
      const s = net.createConnection({ port, host: "127.0.0.1" });
      s.once("connect", () => {
        s.destroy();
        resolve(true);
      });
      s.once("error", () => resolve(false));
    });
    if (up) return;
    await new Promise((r) => setTimeout(r, 250));
  }
}

async function startServer(workspace, port, extraEnv) {
  const nextBin = path.join(PKG_DIR, "node_modules", "next", "dist", "bin", "next");
  // cwd = workspace so lib/mesh-state.ts (process.cwd() + "/.mesh") lands in user space.
  // Pass PKG_DIR as positional so Next reads .next/, next.config, app/ from the package.
  const child = spawn(
    process.execPath,
    [nextBin, "start", "-p", String(port), "-H", "127.0.0.1", PKG_DIR],
    {
      cwd: workspace,
      env: {
        ...process.env,
        ...extraEnv,
      },
      stdio: "inherit",
    }
  );
  const forward = (sig) => {
    if (!child.killed) child.kill(sig);
  };
  process.on("SIGINT", () => forward("SIGINT"));
  process.on("SIGTERM", () => forward("SIGTERM"));
  child.on("exit", (code) => exit(code ?? 0));
  return { child };
}

function printHelp() {
  log("");
  log(`${C.bold}mesh-app${C.reset} — onboarding CLI`);
  log("");
  log("Usage:");
  log("  npx github:danielmedinac22/mesh onboard [options]");
  log("");
  log("Options:");
  log("  -w, --workspace <path>   Workspace dir (default: ./mesh-workspace, remembered)");
  log("  -p, --port <number>      Port to bind (default: 3030+)");
  log("  -y, --yes                Non-interactive: use env vars and defaults");
  log("  -h, --help               Show this help");
  log("");
}

async function onboard(args) {
  banner();
  await checkPrereqs();

  const rl = readline.createInterface({ input: stdin, output: stdout });
  try {
    const ws = await promptWorkspace(rl, args);
    await ensureWorkspace(ws);
    await saveLastWorkspace(ws);

    const envVars = await ensureEnv(ws, rl, args);
    await ensureGhAuth();
    const token = await getGhToken();
    if (token) {
      envVars.GITHUB_TOKEN = token;
      // Append to .env so subsequent runs have it
      const envPath = path.join(ws, ".env");
      const text = await fs.readFile(envPath, "utf8");
      if (!text.includes("GITHUB_TOKEN=")) {
        await fs.writeFile(envPath, text.trimEnd() + `\nGITHUB_TOKEN=${token}\n`);
      }
    }

    rl.close();

    await ensureBuilt();
    const port = args.port ?? (await findFreePort(3030));
    const url = `http://127.0.0.1:${port}`;

    log("");
    log(`${C.cyan}Starting Mesh${C.reset} on ${C.bold}${url}${C.reset}`);
    log("");

    await startServer(ws, port, envVars);
    await waitForPort(port);
    openBrowser(url);
  } catch (e) {
    rl.close();
    log(err(e.message || String(e)));
    exit(1);
  }
}

const args = parseArgs(process.argv.slice(2));
const sub = args._[0];

if (args.help || !sub) {
  printHelp();
  exit(0);
}

if (sub === "onboard") {
  await onboard(args);
} else {
  log(err(`Unknown command: ${sub}`));
  printHelp();
  exit(1);
}
